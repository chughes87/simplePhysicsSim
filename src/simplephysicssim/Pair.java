class Pair
{
	Pair()
	{
		first = 0;
		first = 0;
	}
	Pair(Double x, Double y)
	{
		first = x;
		second = y;
	}
	public double getFirst()
	{
		return first;
	}
	public double getSecond()
	{
		return second;
	}
	public double first;
	public double second;
}